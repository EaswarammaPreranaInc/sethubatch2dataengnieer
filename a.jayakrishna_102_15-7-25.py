# complex object demo program
a = 3 + 4j
print(a) # value of the object 'a' i.e, (3+4j)
print(type(a)) # type of the object 'a' i.e, <class 'complex'>
print(id(a)) # address of the object 'a'  ex:- 2000
print(a . real) # value of the real part object in 'a' i.e, 3.0
print(a . imag) # value of the imaginary part object in 'a' i.e, 4.0
print(type(a . real)) # type of the real part object in 'a' <class 'float'>
print(type(a . imag)) # type of the imaginary part object in 'a' <class 'float'>




