# Write   a  program  to  print  month  name  for  input  month  number  by  using  if  and  elif  (Home  work)

month = input("Enter month number (1 - 12): ")

if month.isdigit():
    m = int(month)
    if m == 1:
        print("January")
    elif m == 2:
        print("February")
    elif m == 3:
        print("March")
    elif m == 4:
        print("April")
    elif m == 5:
        print("May")
    elif m == 6:
        print("June")
    elif m == 7:
        print("July")
    elif m == 8:
        print("August")
    elif m == 9:
        print("September")
    elif m == 10:
        print("October")
    elif m == 11:
        print("November")
    elif m == 12:
        print("December")
    else:
        print("Input should be between 1 and 12")
else:
    print("Input should be an integer") 

